#include "libft.h"

void	ft_putnbr_fd(int nbr, int fd)
{
	if (nbr == 2147483647)
	{
		ft_putstr_fd("2147483647", fd);
		return ;
	}
	if (nbr == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (nbr < 0)
	{
		ft_putchar_fd('-', fd);
		nbr = -nbr;
	}
	if (nbr >= 10)
		ft_putnbr_fd(nbr / 10, fd);
	ft_putchar_fd(nbr % 10 + 48, fd);
}
